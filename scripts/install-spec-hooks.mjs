#!/usr/bin/env node
import fs from 'fs';
import path from 'path';
import { execSync } from 'child_process';

/**
 * Spec-System Hardening — Hook Installer (R2)
 * ============================================
 * Installs the spec validation pre-commit gate into .git/hooks/pre-commit.
 * 
 * RELIABILITY: Creates Layer 2 of the spec validation system.
 * The installed hook calls scripts/validate-specs-precommit.mjs on every commit
 * that touches spec files. Idempotent — safe to run multiple times.
 * 
 * PART OF: spec-system-hardening (specs/spec-system-hardening/spec.md)
 * DOCS: docs/system/spec-driven-development.md (Pre-Commit Hook section)
 * AGENT: opencode-assets/agents/spec.md (Prerequisites)
 * REPO: AGENTS.md (Before any work section)
 */

const HOOK_BLOCK = `#!/bin/sh
# BEGIN spec-validation — auto-generated by install-spec-hooks.mjs
# Part of spec-system-hardening (R2): pre-commit validation gate for spec files.
# Runs validate-specs-precommit.mjs which detects staged spec files and validates.
# Bypass: SKIP_SPEC_CHECK=1 git commit
# CAUTION: CI still enforces the gate regardless of this bypass.
node scripts/validate-specs-precommit.mjs
# END spec-validation
`;

function getGitHooksDir() {
  try {
    const gitDir = execSync('git rev-parse --git-dir', { encoding: 'utf8' }).trim();
    return path.resolve(gitDir, 'hooks');
  } catch (e) {
    console.error('[install-spec-hooks] Not in a git repository.');
    process.exit(2);
  }
}

function isComplexHook(content) {
  // Check for control flow constructs that indicate non-trivial logic
  const controlPattern = /^\s*(if|case|trap|exit)\b/m;
  return controlPattern.test(content);
}

function main() {
  const hooksDir = getGitHooksDir();
  const hookPath = path.join(hooksDir, 'pre-commit');

  if (fs.existsSync(hookPath)) {
    const existing = fs.readFileSync(hookPath, 'utf8');

    // Check if already installed (idempotent)
    if (existing.includes('# BEGIN spec-validation')) {
      console.log('[install-spec-hooks] Spec pre-commit hook already installed. Run again to update.');
      process.exit(0);
    }

    // Check if the existing hook is "complex" — has control flow before our block
    if (isComplexHook(existing)) {
      console.warn('[install-spec-hooks] Existing pre-commit hook is complex (contains if/case/trap/exit).');
      console.warn('[install-spec-hooks] Manual installation required. Add the following to .git/hooks/pre-commit:');
      console.warn('');
      console.warn(HOOK_BLOCK);
      process.exit(1);
    }

    // Simple existing hook — append the block
    const updated = existing.replace(/\n*$/, '') + '\n\n' + HOOK_BLOCK;
    fs.writeFileSync(hookPath, updated, 'utf8');
    try {
      execSync(`chmod +x "${hookPath}"`, { stdio: 'ignore' });
    } catch (_) {
      // chmod may fail on Windows; warn but continue
      console.warn('[install-spec-hooks] Warning: could not set executable permission on hook (expected on Windows — Git Bash manages this).');
    }
    console.log('[install-spec-hooks] Spec pre-commit hook installed. Run `node scripts/install-spec-hooks.mjs` again to update.');
    process.exit(0);
  }

  // No existing hook — write the full file
  fs.writeFileSync(hookPath, HOOK_BLOCK, 'utf8');
  try {
    execSync(`chmod +x "${hookPath}"`, { stdio: 'ignore' });
  } catch (_) {
    console.warn('[install-spec-hooks] Warning: could not set executable permission on hook (expected on Windows — Git Bash manages this).');
  }
  console.log('[install-spec-hooks] Spec pre-commit hook installed. Run `node scripts/install-spec-hooks.mjs` again to update.');
}

main();

export { HOOK_BLOCK, getGitHooksDir, isComplexHook };
