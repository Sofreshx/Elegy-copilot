#!/usr/bin/env node
/**
 * Spec-System Hardening — Index Generator
 * =========================================
 * Generates specs/index.md from all specs/<spec>/spec.md files.
 * 
 * RELIABILITY: The generated index is validated against the filesystem
 * by validate-specs.js --strict (R3 index drift check). CI enforces
 * that the index stays in sync.
 * 
 * PART OF: spec-system-hardening (R3.3 — shared YAML parser)
 */
'use strict';

const fs = require('fs');
const path = require('path');
const { parseFrontmatterYaml } = require('./lib/spec-yaml.js');

const STATUS_ORDER = { draft: 0, approved: 1, implemented: 2, superseded: 3 };

function toPosix(filePath) {
  return String(filePath || '').replace(/\\/g, '/');
}

function matchFrontmatter(text) {
  if (!String(text || '').startsWith('---')) return null;
  const match = String(text).match(/^---\r?\n([\s\S]*?)\r?\n---\r?\n?/);
  if (!match) return null;
  return { full: match[0], yaml: match[1] };
}

function extractFirstSentence(text) {
  const cleaned = String(text || '').replace(/^#+\s+/, '').trim();
  const firstLine = cleaned.split('\n')[0].trim();
  if (!firstLine) return '';
  const dotMatch = firstLine.match(/^([^.!?]*[.!?])/);
  if (dotMatch) return dotMatch[1];
  return firstLine.length > 120 ? firstLine.slice(0, 117) + '...' : firstLine;
}

function collectSpecs(rootPath) {
  const results = [];
  function walk(dir) {
    const entries = fs.readdirSync(dir, { withFileTypes: true });
    for (const entry of entries) {
      const fullPath = path.join(dir, entry.name);
      if (entry.isDirectory()) {
        walk(fullPath);
        continue;
      }
      if (entry.isFile() && entry.name === 'spec.md') {
        results.push(fullPath);
      }
    }
  }
  walk(rootPath);
  return results.sort((a, b) => a.localeCompare(b));
}

function generateIndex(specsPath) {
  const resolvedPath = path.resolve(specsPath);
  if (!fs.existsSync(resolvedPath)) {
    console.error(`specs directory not found: ${resolvedPath}`);
    process.exit(1);
  }

  const specFiles = collectSpecs(resolvedPath);
  const entries = [];

  for (const filePath of specFiles) {
    const content = fs.readFileSync(filePath, 'utf8');
    const fm = matchFrontmatter(content);
    if (!fm) continue;
    const meta = parseFrontmatterYaml(fm.yaml);
    if (!meta.spec_id) continue;

    // Extract Intent: the text after ## Intent heading
    const body = content.slice(fm.full.length);
    const intentMatch = body.match(/^##\s+Intent\s*\n([\s\S]*?)(?=\n##\s)/m);
    const intentSummary = intentMatch ? extractFirstSentence(intentMatch[1]) : '';

    const relPath = toPosix(path.relative(resolvedPath, filePath));
    entries.push({
      filePath: relPath,
      specId: meta.spec_id,
      title: meta.title || meta.spec_id,
      status: meta.status || 'unknown',
      type: meta.type || 'unknown',
      updated: meta.updated || '',
      intent: intentSummary,
    });
  }

  // Sort by status order, then updated descending
  entries.sort((a, b) => {
    const sa = STATUS_ORDER[a.status] ?? 99;
    const sb = STATUS_ORDER[b.status] ?? 99;
    if (sa !== sb) return sa - sb;
    return String(b.updated).localeCompare(String(a.updated));
  });

  const lines = [];
  lines.push('# Specs Index');
  lines.push('');
  lines.push(`> Auto-generated by \`scripts/generate-spec-index.js\`. ${entries.length} specs.`);
  lines.push('');
  lines.push('| Spec | Status | Type | Updated | Intent |');
  lines.push('|------|--------|------|---------|--------|');

  for (const entry of entries) {
    const link = `[${entry.title}](${entry.filePath})`;
    const intentCell = entry.intent ? entry.intent.replace(/\|/g, '\\|') : '';
    lines.push(`| ${link} | ${entry.status} | ${entry.type} | ${entry.updated} | ${intentCell} |`);
  }

  lines.push('');
  return lines.join('\n');
}

function main() {
  const targetPath = path.resolve(process.argv[2] || path.join(process.cwd(), 'specs'));
  const indexContent = generateIndex(targetPath);
  const indexPath = path.join(targetPath, 'index.md');
  fs.writeFileSync(indexPath, indexContent, 'utf8');
  console.log(`specs index written: ${toPosix(indexPath)}`);
}

if (require.main === module) {
  main();
}

module.exports = { generateIndex, collectSpecs };
